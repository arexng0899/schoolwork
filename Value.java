public class Value {
   public static void main(String[] args) {
      int num = 30; // initalizing the variable "num"
         for (int n = 1; n < num; n++) // iterating from 1 to num using a "for" loop
         {
            if (n % 3 == 0 || n % 5 == 0) // "or" operator used for finding values
               System.out.print (n + " "); // printing values
            }
         
    }
}
    
         
         
